package br.com.fiap.to;

public class MicroRegiaoTO {
    private int idMicroRegiao;
    private int qtEquipamento;
    private int qtBateria;
    private int idResponsavel;
    private int idEndereco;

    // Getters e Setters
    public int getIdMicroRegiao() {
        return idMicroRegiao;
    }

    public void setIdMicroRegiao(int idMicroRegiao) {
        this.idMicroRegiao = idMicroRegiao;
    }

    public int getQtEquipamento() {
        return qtEquipamento;
    }

    public void setQtEquipamento(int qtEquipamento) {
        this.qtEquipamento = qtEquipamento;
    }

    public int getQtBateria() {
        return qtBateria;
    }

    public void setQtBateria(int qtBateria) {
        this.qtBateria = qtBateria;
    }

    public int getIdResponsavel() {
        return idResponsavel;
    }

    public void setIdResponsavel(int idResponsavel) {
        this.idResponsavel = idResponsavel;
    }

    public int getIdEndereco() {
        return idEndereco;
    }

    public void setIdEndereco(int idEndereco) {
        this.idEndereco = idEndereco;
    }
}

