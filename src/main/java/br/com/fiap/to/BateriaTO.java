package br.com.fiap.to;

public class BateriaTO {
    private int idBateria;
    private int qtArmazenadaBateria;
    private int capacidade;
    private int idMicroRegiao;

    // Getters e Setters
    public int getIdBateria() {
        return idBateria;
    }

    public void setIdBateria(int idBateria) {
        this.idBateria = idBateria;
    }

    public int getQtArmazenadaBateria() {
        return qtArmazenadaBateria;
    }

    public void setQtArmazenadaBateria(int qtArmazenadaBateria) {
        this.qtArmazenadaBateria = qtArmazenadaBateria;
    }

    public int getCapacidade() {
        return capacidade;
    }

    public void setCapacidade(int capacidade) {
        this.capacidade = capacidade;
    }

    public int getIdMicroRegiao() {
        return idMicroRegiao;
    }

    public void setIdMicroRegiao(int idMicroRegiao) {
        this.idMicroRegiao = idMicroRegiao;
    }

}